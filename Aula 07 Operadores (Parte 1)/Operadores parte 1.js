/*
Operadores aritméticos
São operadores utilizados para fazer contas
+  Soma             5 +  2 = 7
-  Subtração        5 -  2 = 3
*  Multiplição      5 *  2 = 10
/  Divisão          5 /  2 = 5.5
%  resto da divisao 5 %  2 = 1
** potencia         5 ** 2 = 25
*/

/*
Ordem de precedência

()      parênteses
**      potencia 
* / %   Multiplição Divisão resto da divisao
+ -     Soma Subtração  
*/

/*
Operadores de atribuição

var a = 5 + 3               Variavel recebe o valor 8
var b = a % 5               Variavel recebe o valor 3
var c = 5 * (b ** 2)        Variavel recebe o valor 45
var d = 10 - (a / 2)        Variavel recebe o valor 6
var e = (6 * 2) / d         Variavel recebe o valor 2
var f = (b % e) + (4 / e)   Variavel recebe o valor 3
*/

/*
Operadores de auto-atribuição
var n = 3          
 Normal      Simplificado     valor
n = n + 4     n  +=  4    Agora n vale 7
n = n - 5     n  -=  5    Agora n vale 2
n = n * 4     n  *=  4    Agora n vale 8
n = n / 2     n  /=  2    Agora n vale 4
n = n ** 2    n  **= 2    Agora n vale 16
n = n % 5     n  %=  5    Agora n vale 7
*/

/*
Operadores de incremento
x++ incrementa + 1 em x
x-- decrementa - 1 em x

exemplo:
x = 10
x++ retorna 11
x-- retorna 9

  Normal      auto-atribuição  incremento
x = x + 1         x += 1          x++
x = x - 1         x -= 1          x--





*/